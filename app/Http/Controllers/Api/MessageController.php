     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate(
            [
                'email' => 'string',
                'text' => 'string',
            ]
        );

        $data = $request->all();
        $message = new Message();

        $message->email = $data['email'];
        $message->text = $data['text'];
        $message->apartment_id = $data['apartment_id'];

        $message->save();
    }

